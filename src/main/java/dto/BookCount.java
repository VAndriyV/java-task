package dto;

import entities.Book;

public class BookCount {
    public Book book;
    public int count;

    public BookCount() {
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
